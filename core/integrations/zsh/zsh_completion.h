#pragma once

#include <ostream>
#include <string>

#include "App.h"
#include "Commander.h"
#include "Config.h"

namespace drea::core::integrations::Zsh {

// Escape single quotes for zsh single-quoted strings: ' -> '\''
static std::string escape( std::string_view s )
{
	std::string	res;
	res.reserve( s.size() );
	for( char c: s ){
		if( c == '\'' ){
			res += "'\\''";
		}else{
			res += c;
		}
	}
	return res;
}

static void generateAutoCompletion( const drea::core::App & app, std::ostream & out )
{
	const std::string	name = app.name();

	out << "#compdef " << name << "\n";
	out << "# zsh completion for " << name << " (generated by drea)\n\n";

	// Per-command option descriptor function. Lists local + global options.
	out << "_" << name << "_opts_for() {\n";
	out << "    local cmd=\"$1\"\n";
	out << "    case \"$cmd\" in\n";
	app.commander().commands( [&out, &app]( const Command & cmd ){
		if( cmd.mHidden ){
			return;
		}
		std::string path = cmd.mParentCommand.empty() ? cmd.mName : cmd.mParentCommand + "." + cmd.mName;
		// zsh uses spaces between subcommands
		std::string display = path;
		for( auto & ch: display ){
			if( ch == '.' ){
				ch = ' ';
			}
		}
		out << "        '" << escape( display ) << "')\n";
		out << "            _arguments";
		auto emitOption = [&out, &app]( const std::string & optName ){
			auto opt = app.config().find( optName );
			if( !opt ){
				return;
			}
			out << " \\\n                '--" << escape( opt->mName );
			if( !opt->mParamName.empty() ){
				out << "=[" << escape( opt->mDescription ) << "]:" << escape( opt->mParamName ) << ":";
			}else{
				out << "[" << escape( opt->mDescription ) << "]";
			}
			out << "'";
			if( !opt->mShortVersion.empty() ){
				out << " \\\n                '-" << escape( opt->mShortVersion );
				if( !opt->mParamName.empty() ){
					out << "[" << escape( opt->mDescription ) << "]:" << escape( opt->mParamName ) << ":";
				}else{
					out << "[" << escape( opt->mDescription ) << "]";
				}
				out << "'";
			}
		};
		for( const auto & o: cmd.mLocalParameters ){
			emitOption( o );
		}
		for( const auto & o: cmd.mGlobalParameters ){
			emitOption( o );
		}
		out << "\n            ;;\n";
	});
	out << "    esac\n";
	out << "}\n\n";

	// Main dispatcher
	out << "_" << name << "() {\n";
	out << "    local -a _top_commands\n";
	out << "    _top_commands=(\n";
	app.commander().commands( [&out]( const Command & cmd ){
		if( cmd.mHidden || !cmd.mParentCommand.empty() ){
			return;
		}
		out << "        '" << escape( cmd.mName ) << ":" << escape( cmd.mDescription ) << "'\n";
	});
	out << "    )\n";

	out << "    local context state line\n";
	out << "    typeset -A opt_args\n";
	out << "    _arguments -C \\\n";
	out << "        '1: :->cmd' \\\n";
	out << "        '*::arg:->args'\n";
	out << "    case $state in\n";
	out << "        cmd)\n";
	out << "            _describe -t commands '" << escape( name ) << " command' _top_commands\n";
	out << "            ;;\n";
	out << "        args)\n";
	out << "            local path=\"${words[1]}\"\n";
	out << "            local depth=2\n";
	out << "            # Walk subcommands to build a space-separated path\n";
	out << "            while (( depth <= ${#words} )); do\n";
	out << "                local candidate=\"${path} ${words[$depth]}\"\n";
	out << "                local matched=0\n";
	app.commander().commands( [&out]( const Command & cmd ){
		if( cmd.mHidden || cmd.mParentCommand.empty() ){
			return;
		}
		std::string display = cmd.mParentCommand + "." + cmd.mName;
		for( auto & ch: display ){
			if( ch == '.' ){
				ch = ' ';
			}
		}
		out << "                [[ \"$candidate\" == '" << escape( display ) << "' ]] && matched=1\n";
	});
	out << "                (( matched )) || break\n";
	out << "                path=\"$candidate\"\n";
	out << "                (( depth++ ))\n";
	out << "            done\n";
	out << "            _" << name << "_opts_for \"$path\"\n";
	out << "            ;;\n";
	out << "    esac\n";
	out << "}\n\n";
	out << "_" << name << " \"$@\"\n";
}

}
